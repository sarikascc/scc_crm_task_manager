'use client'

import { UserData } from '@/lib/users/actions'
import { EmptyState } from '@/app/components/empty-state'
import { Tooltip } from '@/app/components/ui/tooltip'

interface UsersTableProps {
    users: UserData[]
    currentUserId: string
    canWrite: boolean
    onEdit: (user: UserData) => void
    onChangePassword: (user: UserData) => void
    onDelete: (user: UserData) => void
}

function StatusPill({ status }: { status: boolean }) {
    const style = status
        ? { bg: 'bg-emerald-50', text: 'text-emerald-700', dot: 'bg-emerald-600', ring: 'ring-emerald-600/20' }
        : { bg: 'bg-red-50', text: 'text-red-700', dot: 'bg-red-600', ring: 'ring-red-600/20' }

    return (
        <span
            className={`inline-flex items-center gap-1.5 rounded-full px-2 py-1 text-xs font-medium ${style.bg} ${style.text} ring-1 ring-inset ${style.ring}`}
        >
            <span className={`h-1.5 w-1.5 rounded-full ${style.dot}`}></span>
            {status ? 'Active' : 'Inactive'}
        </span>
    )
}

function RolePill({ role }: { role: string }) {
    const styles = {
        admin: { bg: 'bg-violet-50', text: 'text-violet-700', ring: 'ring-violet-600/20' },
        manager: { bg: 'bg-sky-50', text: 'text-sky-700', ring: 'ring-sky-600/20' },
        staff: { bg: 'bg-amber-50', text: 'text-amber-700', ring: 'ring-amber-600/20' },
        client: { bg: 'bg-slate-50', text: 'text-slate-700', ring: 'ring-slate-600/20' },
    }

    const style = styles[role as keyof typeof styles] || styles.staff

    return (
        <span
            className={`inline-flex items-center rounded-md px-2 py-1 text-xs font-medium capitalize ${style.bg} ${style.text} ring-1 ring-inset ${style.ring}`}
        >
            {role}
        </span>
    )
}

export function UsersTable({ users, currentUserId, canWrite, onEdit, onChangePassword, onDelete }: UsersTableProps) {
    if (users.length === 0) {
        return (
            <div className="flex h-full w-full min-h-[500px] items-center justify-center bg-white">
                <div className="w-full max-w-lg">
                    <EmptyState
                        variant="users"
                        title="No users found"
                        description="Add a new user to get started."
                    />
                </div>
            </div>
        )
    }

    return (
        <div className="h-full w-full bg-white">
            <table className="w-full table-fixed divide-y divide-gray-100">
                <thead className="sticky top-0 z-10 bg-white">
                    <tr className="bg-gray-50/50">
                        <th className="w-[45%] sm:w-[25%] px-4 sm:px-6 py-3 text-left text-xs font-semibold uppercase tracking-wider text-gray-500 transition-all duration-200">
                            User
                        </th>
                        <th className="hidden sm:table-cell sm:w-[30%] px-6 py-3 text-left text-xs font-semibold uppercase tracking-wider text-gray-500 transition-all duration-200">
                            Email
                        </th>
                        <th className="w-[20%] sm:w-[15%] px-4 sm:px-6 py-3 text-left text-xs font-semibold uppercase tracking-wider text-gray-500 transition-all duration-200">
                            Role
                        </th>
                        <th className="w-[17%] sm:w-[15%] px-4 sm:px-6 py-3 text-left text-xs font-semibold uppercase tracking-wider text-gray-500 transition-all duration-200">
                            Status
                        </th>
                        <th className="w-[18%] sm:w-[15%] px-4 sm:px-6 py-3 text-right text-xs font-semibold uppercase tracking-wider text-gray-500 transition-all duration-200">
                            {canWrite ? 'Actions' : 'View'}
                        </th>
                    </tr>
                </thead>
                <tbody className="divide-y divide-gray-50 bg-white">
                    {users.map((user) => (
                        <tr
                            key={user.id}
                            className="group transition-all duration-200 hover:bg-slate-50 cursor-pointer"
                            onClick={() => onEdit(user)}
                        >
                            <td className="px-4 sm:px-6 py-3">
                                <div className="flex items-center gap-2 sm:gap-3">
                                    <div className="flex h-8 w-8 sm:h-9 sm:w-9 items-center justify-center rounded-full bg-gradient-to-br from-cyan-500 to-blue-600 text-xs sm:text-sm font-bold text-white shadow-sm flex-shrink-0 ring-2 ring-white">
                                        {user.full_name?.substring(0, 2).toUpperCase() || 'U'}
                                    </div>
                                    <div className="flex flex-col min-w-0">
                                        <span className="truncate text-sm sm:text-base font-semibold text-gray-900 leading-tight">{user.full_name}</span>
                                    </div>
                                </div>
                            </td>
                            <td className="hidden sm:table-cell px-6 py-3">
                                <div className="truncate text-sm text-gray-500">{user.email}</div>
                            </td>
                            <td className="px-4 sm:px-6 py-3 text-sm">
                                <RolePill role={user.role} />
                            </td>
                            <td className="px-4 sm:px-6 py-3 text-sm">
                                <StatusPill status={user.is_active} />
                            </td>
                            <td className="px-4 sm:px-6 py-3 text-right text-sm" onClick={(e) => e.stopPropagation()}>
                                <div className="flex items-center justify-end gap-2">
                                    {canWrite && (
                                        <>
                                            <Tooltip content="Edit user profile" position="left">
                                                <button
                                                    onClick={() => onEdit(user)}
                                                    className="rounded-lg p-2 text-gray-400 transition-colors hover:bg-indigo-50 hover:text-indigo-600"
                                                >
                                                    <svg className="h-4.5 w-4.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                                                        <path strokeLinecap="round" strokeLinejoin="round" d="M11 5H6a2 2 0 00-2 2v11a2 2 0 002 2h11a2 2 0 002-2v-5m-1.414-9.414a2 2 0 112.828 2.828L11.828 15H9v-2.828l8.586-8.586z" />
                                                    </svg>
                                                </button>
                                            </Tooltip>
                                            <Tooltip content="Reset account password" position="left">
                                                <button
                                                    onClick={() => onChangePassword(user)}
                                                    className="rounded-lg p-2 text-gray-400 transition-colors hover:bg-cyan-50 hover:text-cyan-600"
                                                >
                                                    <svg className="h-4.5 w-4.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                                                        <path strokeLinecap="round" strokeLinejoin="round" d="M15 7a2 2 0 012 2m4 0a6 6 0 01-7.743 5.743L11 17H9v2H7v2H4a1 1 0 01-1-1v-2.586a1 1 0 01.293-.707l5.964-5.964A6 6 0 1121 9z" />
                                                    </svg>
                                                </button>
                                            </Tooltip>
                                            {user.id !== currentUserId && (
                                                <Tooltip content="Deactivate user" position="left">
                                                    <button
                                                        onClick={() => onDelete(user)}
                                                        className="rounded-lg p-2 text-gray-400 transition-colors hover:bg-red-50 hover:text-red-600"
                                                    >
                                                        <svg className="h-4.5 w-4.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                                                            <path strokeLinecap="round" strokeLinejoin="round" d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16" />
                                                        </svg>
                                                    </button>
                                                </Tooltip>
                                            )}
                                        </>
                                    )}
                                </div>
                            </td>
                        </tr>
                    ))}
                </tbody>
            </table>
        </div>
    )
}
